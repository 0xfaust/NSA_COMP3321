'''Lecture 03, Exercise 08

Previously we printed out many copies of a string using the * operator. Use a
while loop to print out 10 copies of your favourite snack. Each copy can be on
it's own line, that's fine.
'''

snack = 'coffee'
i = 0

while (i < 10):
    print(snack)
    i += 1

